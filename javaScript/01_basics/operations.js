// Postfix increment

let x2 = 3;
const y2 = ++x2;
// x is 4; y is 4
console.log(`x2:${x2}, y2:${y2}`);

let x3 = 3n;
const y3 = ++x3;
console.log(`x3:${x3}, y3:${y3}`);
